public class Human {


    private int income;
    private String name;

    public Human(int income, String name) {
        this.income = income;
        this.name = name;
    }

    public int getIncome() {

        return income;
    }

    public void setIncome(int income) {
        this.income = income;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}





