public class Pet {
    private int mNumberOfPets;
    private String mColour;
    private int mNumberOfFeet;
    private String mSpecies;
    private String mOwner;

public Pet(int numberOfPets, String colour, int numberOfFeet, String species, String owner){
    mNumberOfPets = numberOfPets;
    mColour = colour;
    mNumberOfFeet = numberOfFeet;
    mSpecies = species;
    mOwner = owner;
}




}
